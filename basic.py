# Create a program that asks the user for two numbers and 
# prints their sum, difference, product, and quotient.

# first number
user = int(input("Give a number for sum: "))

# second number
user2 = int(input("Give a second number for sum: "))

sum1 = user + user2
print(sum1)

sum2 = user - user2
print(sum2)

sum3 = user * user2
print(sum3)

sum4 = int(user / user2)
print(sum4)
